extern USBD_HandleTypeDef hUsbDeviceFS;
#define MIC_SAMPLES_PER_PACKET   (AUDIO_IN_PACKET / 2)

// I2S raw: 32-bit/sample, x2 kênh (L,R), x2 buffer (đôi, half0/half1)
static int32_t i2s_raw_buf[AUDIO_IN_PACKET * 2]; // một sample là 4bytes -> 48 * 4

void MIC_I2S_Start(void)
{
    HAL_I2S_Receive_DMA(&hi2s1, (uint16_t*)i2s_raw_buf, AUDIO_IN_PACKET * 2);
}

static void I2S_ExtractMono(int32_t *raw, uint8_t half)
{
    USBD_AUDIO_HandleTypeDef *haudio = hUsbDeviceFS.pClassData;
    if (haudio == NULL) return;
    int16_t *dest = haudio->in_buffer + (AUDIO_IN_PACKET / 2) * half;
    for (uint16_t i = 0; i < MIC_SAMPLES_PER_PACKET; i++)
    {
        int32_t sample = (int16_t)(raw[i * 2] & 0xFFFF);
        sample *= 4;                          // SỬA: gain x8, thử tăng/giảm tuỳ nghe
        if (sample > 32767) sample = 32767;   // clamp tránh clip/overflow
        if (sample < -32768) sample = -32768;
        dest[i] = (int16_t)sample;
    }
}

void HAL_I2S_RxHalfCpltCallback(I2S_HandleTypeDef *hi2s)
{
	  I2S_ExtractMono(i2s_raw_buf, 0);
}

void HAL_I2S_RxCpltCallback(I2S_HandleTypeDef *hi2s)
{
	  I2S_ExtractMono(&i2s_raw_buf[MIC_SAMPLES_PER_PACKET * 2], 1);
}




